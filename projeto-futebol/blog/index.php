          <h2 class="blog-post-title">Regras futebol </h2>
  
          <p>As regras do futebol são definidas pela International Football Association Board (IFAB) e são compostas por 17 regras principais. Aqui estão algumas das regras principais:</p>
  
          <p>1.	Número de Jogadores: Cada equipe deve ter 11 jogadores, incluindo um goleiro.</p>
  
          <p>2.	Duração do Jogo: Uma partida de futebol tem duração de 90 minutos, divididos em dois tempos de 45 minutos, com um intervalo de 15 minutos.</p>
  
          <p>3.	Bola: A bola deve ser esférica, fabricada em couro ou outro material apropriado, ter uma circunferência entre 68cm e 70cm, e um peso entre 410g e 450g</p>
  
          <p>4.	Campo: O campo de jogo será retangular. O comprimento da linha lateral deverá ser superior ao comprimento da linha de meta</p>
          
          <p>5.	Árbitro: O jogo é supervisionado por um árbitro, que pode expulsar jogadores por infrações graves.</p>
          
          <p>6.	Uso das Mãos: É proibido o uso das mãos para o manejo da bola, exceto pelo goleiro na sua área.</p>

